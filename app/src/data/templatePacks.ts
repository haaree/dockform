function f(type: string, label: string, o: {
  ph?: string; help?: string; def?: string; req?: boolean; ro?: boolean;
  search?: boolean; idx?: boolean; opts?: string[]; min?: string; max?: string; err?: string;
  repeatable?: boolean; tableLayout?: boolean; seedRows?: string[];
} = {}): FieldDef {
  return {
    type, label,
    placeholder: o.ph || '', helpText: o.help || '', defaultValue: o.def || '',
    required: o.req || false, readOnly: o.ro || false,
    hidden: false, searchable: o.search || false, indexed: o.idx || false,
    options: o.opts || [],
    validation: { min: o.min || '', max: o.max || '', pattern: '', message: o.err || '' },
    logic: [],
    ...(o.repeatable ? { repeatable: true } : {}),
    ...(o.tableLayout ? { tableLayout: true } : {}),
    ...(o.seedRows ? { seedRows: o.seedRows } : {}),
  };
}

